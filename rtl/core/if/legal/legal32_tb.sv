`include "isa.svh"
// ================================================================================
//  legal32_tb -- dual-model: the DUT's case tree vs the spec's opcode-map table
//  transcribed as casez patterns; structured + random + directed sweeps
// ================================================================================

module legal32_tb;
  logic [31:0] instr;
  logic        illegal;
  int          errors = 0;

  legal32 dut (.instr, .illegal);

  // the RV32IM + Zicsr opcode map, one pattern per table row
  function automatic logic isLegal(input logic [31:0] i);
    casez (i)
      32'b?????????????????????????0110111,   // LUI
      32'b?????????????????????????0010111,   // AUIPC
      32'b?????????????????????????1101111,   // JAL
      32'b?????????????????000?????1100111,   // JALR
      32'b?????????????????000?????1100011,   // BEQ
      32'b?????????????????001?????1100011,   // BNE
      32'b?????????????????1???????1100011,   // BLT/BGE/BLTU/BGEU
      32'b?????????????????00??????0000011,   // LB/LH
      32'b?????????????????010?????0000011,   // LW
      32'b?????????????????10??????0000011,   // LBU/LHU
      32'b?????????????????00??????0100011,   // SB/SH
      32'b?????????????????010?????0100011,   // SW
      32'b?????????????????000?????0010011,   // ADDI
      32'b?????????????????01??????0010011,   // SLTI/SLTIU
      32'b?????????????????100?????0010011,   // XORI
      32'b?????????????????11??????0010011,   // ORI/ANDI
      32'b0000000??????????001?????0010011,   // SLLI
      32'b0?00000??????????101?????0010011,   // SRLI/SRAI
      32'b0000000??????????????????0110011,   // op-reg base (all eight f3)
      32'b0100000??????????000?????0110011,   // SUB
      32'b0100000??????????101?????0110011,   // SRA
      32'b0000001??????????????????0110011,   // RV32M (all eight f3)
      32'b?????????????????00??????0001111,   // FENCE / FENCE.I
      INSTR_ECALL, INSTR_EBREAK, INSTR_MRET, INSTR_WFI,
      32'b?????????????????001?????1110011,   // CSRRW
      32'b?????????????????01??????1110011,   // CSRRS/CSRRC
      32'b?????????????????101?????1110011,   // CSRRWI
      32'b?????????????????11??????1110011:   // CSRRSI/CSRRCI
        return 1'b1;
      default:
        return 1'b0;
    endcase
  endfunction

  task automatic chk(input logic [31:0] i, input string note);
    instr = i;
    #1;
    if (illegal !== !isLegal(i)) begin
      $error("%-14s instr=%h dut=%b golden=%b", note, i, illegal, isLegal(i));
      errors++;
    end
  endtask

  task automatic chkIs(input logic [31:0] i, input logic wantIllegal,
                       input string note);
    instr = i;
    #1;
    if (illegal !== wantIllegal || isLegal(i) !== !wantIllegal) begin
      $error("%-14s instr=%h dut=%b golden=%b want illegal=%b",
             note, i, illegal, isLegal(i), wantIllegal);
      errors++;
    end
  endtask

  initial begin
    // ---- structured: every {f7, f3, opcode} against the golden, two fills ------
    for (int pass = 0; pass < 2; pass++) begin
      for (int c = 0; c < (1 << 17); c++) begin
        automatic logic [16:0] cb  = 17'(c);
        automatic logic [31:0] mid = (pass == 0) ? 32'h0 : 32'($urandom());
        chk({cb[16:10], mid[24:20], mid[19:15], cb[9:7], mid[11:7], cb[6:0]},
            "structured");
      end
    end

    // ---- random full-width -----------------------------------------------------
    for (int k = 0; k < 200000; k++)
      chk(32'($urandom()), "random");

    // ---- directed: the exact-match system space and its near misses ------------
    chkIs(INSTR_ECALL,                  1'b0, "ecall");
    chkIs(INSTR_EBREAK,                 1'b0, "ebreak");
    chkIs(INSTR_MRET,                   1'b0, "mret");
    chkIs(INSTR_WFI,                    1'b0, "wfi");
    chkIs(INSTR_ECALL  | 32'h0008_0000, 1'b1, "ecall rs1!=0");
    chkIs(INSTR_ECALL  | 32'h0000_0080, 1'b1, "ecall rd!=0");
    chkIs(32'h0030_0073,                1'b1, "funct12=003");
    chkIs(INSTR_MRET   ^ 32'h0010_0000, 1'b1, "mret funct12 off");
    chkIs(INSTR_WFI    | 32'h0008_0000, 1'b1, "wfi rs1!=0");

    // ---- directed: known legal / known illegal spot checks ---------------------
    chkIs(NOP_INSTR,                    1'b0, "nop");
    chkIs(32'h0000_0000,                1'b1, "all zeros");
    chkIs(32'hFFFF_FFFF,                1'b1, "all ones");
    chkIs(32'h00B5_0533,                1'b0, "add a0,a0,a1");
    chkIs(32'h4015_5513,                1'b0, "srai a0,a0,1");
    chkIs(32'hC015_5513,                1'b1, "srai bad f7");
    chkIs({32'h0015_0513 & 32'hFFFF_FFFC} | 32'h2, 1'b1, "instr[1:0]=10");

    if (errors == 0) $display("PASS  legal32");
    else             $fatal(1, "FAIL  legal32 (%0d errors)", errors);
    $finish;
  end
endmodule

